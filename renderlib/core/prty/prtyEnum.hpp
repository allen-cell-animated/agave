#pragma once

#include "core/prty/prtyInt8.hpp"

#include <vector>

//============================================================================
//============================================================================
class prtyEnum : public prtyInt8
{
public:
  //--------------------------------------------------------------------
  //--------------------------------------------------------------------
  prtyEnum();

  //--------------------------------------------------------------------
  //--------------------------------------------------------------------
  prtyEnum(const std::string& i_Name);

  //--------------------------------------------------------------------
  //--------------------------------------------------------------------
  prtyEnum(const std::string& i_Name, const char& i_InitialValue);

  //--------------------------------------------------------------------
  //	The type of property it is
  //--------------------------------------------------------------------
  virtual const char* GetType();

  //--------------------------------------------------------------------
  //	Set an EnumTag	- these will get displayed (in a combobox for
  //	instance)
  //--------------------------------------------------------------------
  void SetEnumTag(int i_Index, const std::string& i_Tag);
  const std::string& GetEnumTag(int i_Index);

  //--------------------------------------------------------------------
  // Return number of enumeration tags
  //--------------------------------------------------------------------
  int GetNumTags() const;

  //--------------------------------------------------------------------
  // Clear enum tags
  //--------------------------------------------------------------------
  void ClearTags();

public:
  std::vector<std::string> m_EnumTags; // tags to display for this enum
};
